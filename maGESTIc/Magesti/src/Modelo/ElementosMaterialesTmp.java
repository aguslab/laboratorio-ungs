package Modelo;

public class ElementosMaterialesTmp {
	
	private String nomb_Producto;
	private Integer cant_Producto;
	private String calidad;
	private String variante;
	private Integer gramaje;
	private String tamanio_hoja;
	private Integer poses_x_pliego;
	private Integer pliegos_demasia;
	private Integer pliegos_x_hoja;
	private Integer hojas;
	private Integer pliegos_netos;
	
	
	
	public ElementosMaterialesTmp(String nomb_Producto, Integer cant_Producto,
			String calidad, String variante, Integer gramaje,
			String tamanio_hoja, Integer poses_x_pliego,
			Integer pliegos_demasia, Integer pliegos_x_hoja, Integer hojas,
			Integer pliegos_netos) {
		super();
		this.nomb_Producto = nomb_Producto;
		this.cant_Producto = cant_Producto;
		this.calidad = calidad;
		this.variante = variante;
		this.gramaje = gramaje;
		this.tamanio_hoja = tamanio_hoja;
		this.poses_x_pliego = poses_x_pliego;
		this.pliegos_demasia = pliegos_demasia;
		this.pliegos_x_hoja = pliegos_x_hoja;
		this.hojas = hojas;
		this.pliegos_netos = pliegos_netos;
	}



	public String getNomb_Producto() {
		return nomb_Producto;
	}



	public void setNomb_Producto(String nomb_Producto) {
		this.nomb_Producto = nomb_Producto;
	}



	public Integer getCant_Producto() {
		return cant_Producto;
	}



	public void setCant_Producto(Integer cant_Producto) {
		this.cant_Producto = cant_Producto;
	}



	public String getCalidad() {
		return calidad;
	}



	public void setCalidad(String calidad) {
		this.calidad = calidad;
	}



	public String getVariante() {
		return variante;
	}



	public void setVariante(String variante) {
		this.variante = variante;
	}



	public Integer getGramaje() {
		return gramaje;
	}



	public void setGramaje(Integer gramaje) {
		this.gramaje = gramaje;
	}



	public String getTamanio_hoja() {
		return tamanio_hoja;
	}



	public void setTamanio_hoja(String tamanio_hoja) {
		this.tamanio_hoja = tamanio_hoja;
	}



	public Integer getPoses_x_pliego() {
		return poses_x_pliego;
	}



	public void setPoses_x_pliego(Integer poses_x_pliego) {
		this.poses_x_pliego = poses_x_pliego;
	}



	public Integer getPliegos_demasia() {
		return pliegos_demasia;
	}



	public void setPliegos_demasia(Integer pliegos_demasia) {
		this.pliegos_demasia = pliegos_demasia;
	}



	public Integer getPliegos_x_hoja() {
		return pliegos_x_hoja;
	}



	public void setPliegos_x_hoja(Integer pliegos_x_hoja) {
		this.pliegos_x_hoja = pliegos_x_hoja;
	}



	public Integer getHojas() {
		return hojas;
	}



	public void setHojas(Integer hojas) {
		this.hojas = hojas;
	}



	public Integer getPliegos_netos() {
		return pliegos_netos;
	}



	public void setPliegos_netos(Integer pliegos_netos) {
		this.pliegos_netos = pliegos_netos;
	}
	
	
	
	
	
	

}

